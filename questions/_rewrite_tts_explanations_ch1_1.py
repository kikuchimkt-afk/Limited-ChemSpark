def main() -> None:
    path = Path(__file__).with_name("ch1-1.json")
    data = json.loads(path.read_text("utf-8"))

    checked = set()
    updated = 0
    for q in data["questions"]:
        qid = q["id"]
        if qid not in REWRITES:
            continue
        checked.add(qid)
        new_te = REWRITES[qid]
        for banned in ["選択肢一", "選択肢二", "選択肢三", "選択肢四",
                       "選択肢五", "選択肢六", "選択肢七", "選択肢八", "選択肢九"]:
            assert banned not in new_te, f"{qid}: still contains {banned}"
        q["tts_explanation"] = new_te
        updated += 1

    missing = set(REWRITES) - checked
    if missing:
        raise SystemExit(f"IDs in REWRITES not found in data: {sorted(missing)}")

    # re-serialise preserving utf-8 and indentation matching existing style.
    path.write_text(
        json.dumps(data, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print(f"Updated {updated} tts_explanation entries in {path.name}.")


if __name__ == "__main__":
    main()
